package operators;

public class EqualityAndRelationalOperator {

	// == Equal to
	// != not Equal
	// > Greater than
	// >= Greater than Equal
	//< less than
	//<= less than Equal
	
	
	
	public static void main(String[] args) {
		
		int number1=39;
		int number2=49;
		
		if (number1 == number2) {
			System.out.println("Number1 and Number 2 are Equal");
		}
		
		if (number1 != number2) {
			System.out.println("Number1 and Number 2 are not Equal");
		}
		
		if (number1 > number2) {
			System.out.println("Number1 is greater than Number 2");
		}
		
		
		if (number1 >= number2) {
			System.out.println("Number1 and Number 2 are greater Equal");
		}
		
		if (number1 < number2) {
			System.out.println("Number1 is less than Number2");
		}
		
		if (number1 <= number2) {
			System.out.println("Number1 less than Equal Number2 ");
		}
		}
	}

